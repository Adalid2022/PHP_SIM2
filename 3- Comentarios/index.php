        Para agregar comentarios en una sola linea en PHP, se utiliza la siguiente Sintaxis <br>
        <code>
            &lt;?php <br>
             // Ejemplo de comentario en una sola linea
             <br>
             # Este es un comentario en una sola línea             
             <br>
            ?&gt;
        </code>
        <br>
        Para agregar un comentario en múltiples líneas, se puede utilizar la siguiente sintaxis. <br>
        <code>
            &lt;?php <br>
             /* <br> 
             Ejemplo de comentarios en multilinea
             <br>
             Otra línea de comentario <br> */             
             <br>
            ?&gt;
        </code>
