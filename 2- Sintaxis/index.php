    <h1>
        Sintaxis en PHP
    </h1>
    <p class="titulo">
        Para crear contenido PHP, se utiliza la siguiente Sintaxis <br>
        <code>
            &lt;?php <br>
            ?&gt;
        </code>
        <br>
        Todo el contenido que se encuentre entre estas etiquetas, sera ejecutado, en el lado del servidor.
    </p>
    <h3 class="titulo">
        Ejemplo
    </h3>
    <code>
        &lt;?php <br>
            &nbsp;&nbsp;&nbsp;echo "Este es un ejemplo de codigo PHP"; <br>            
        ?&gt;
    </code>
